// Language management functions
let currentLanguage = 'en';

function setLanguage(lang) {
    currentLanguage = lang;
    localStorage.setItem('selectedLanguage', lang);
    
    // Update HTML lang and dir attributes
    document.documentElement.lang = lang;
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
    
    // Update all translatable elements
    updateTranslations();
    
    // Update language toggle button
    updateLanguageButton();
}

function getTranslation(key) {
    return translations[currentLanguage][key] || key;
}

function updateTranslations() {
    // Update all elements with data-translate attribute
    const elements = document.querySelectorAll('[data-translate]');
    elements.forEach(element => {
        const key = element.getAttribute('data-translate');
        const translation = getTranslation(key);
        
        if (element.tagName === 'INPUT') {
            if (element.type === 'text' || element.type === 'email' || element.type === 'search') {
                element.placeholder = translation;
            }
        } else if (element.tagName === 'TEXTAREA') {
            element.placeholder = translation;
        } else {
            element.textContent = translation;
        }
    });
    
    // Update dynamic cart content when language changes
    if (window.updateCart && typeof window.updateCart === 'function') {
        window.updateCart();
    }
}

function updateLanguageButton() {
    const langButton = document.getElementById('languageToggle');
    if (langButton) {
        langButton.textContent = currentLanguage === 'en' ? 'العربية' : 'English';
        langButton.title = currentLanguage === 'en' ? 'Switch to Arabic' : 'Switch to English';
    }
}

function toggleLanguage() {
    const newLang = currentLanguage === 'en' ? 'ar' : 'en';
    setLanguage(newLang);
}

// Initialize language on page load
function initializeLanguage() {
    const savedLanguage = localStorage.getItem('selectedLanguage') || 'en';
    setLanguage(savedLanguage);
}

// Export functions for global use
window.setLanguage = setLanguage;
window.toggleLanguage = toggleLanguage;
window.getTranslation = getTranslation;
window.initializeLanguage = initializeLanguage;
